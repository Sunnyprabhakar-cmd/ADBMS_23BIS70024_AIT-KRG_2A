-- Find the top earner in each department.
SELECT
	D.DEPT_NAME, E.NAME, E.SALARY
FROM
	EMPLOYEE AS E
INNER JOIN
	DEPARTMENT AS D
ON
	D.ID = E.DEPT_ID
WHERE
	E.SALARY
IN (
	SELECT MAX(E2.SALARY)
	FROM EMPLOYEE AS E2
	WHERE E2.DEPT_ID = E.DEPT_ID
)
ORDER BY
	D.DEPT_NAME;
