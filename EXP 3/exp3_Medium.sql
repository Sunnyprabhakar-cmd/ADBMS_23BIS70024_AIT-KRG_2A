CREATE DATABASE EXP3;
USE EXP3;

-- Create Input Tables: Department and Employee.
CREATE TABLE Department (
	ID INT PRIMARY KEY, 
	DEPT_NAME VARCHAR(50)
);

CREATE TABLE Employee (
	ID INT,
	NAME VARCHAR(50),
	SALARY INT,
	DEPT_ID INT,
	FOREIGN KEY (DEPT_ID) REFERENCES Department(ID)
);

-- Populate the input data.
